// 2.2 Data types in java

// There are 2 types of data types in Java:
// Primitive and Non-Primitive

// Primitive data types:

// There are eight primitive data types in Java
//  1) boolean: The type whose values are either true or false
// 2) char: the character type whose values are 16-bit Unicode characters integer types: 
// 3) byte
// 4) short
// 5) int
// 6) long

// floating-point types:
// 7) float 
// 8) double
// Size, range and default values of the data types:
// boolean: 
// Size= 1 bit, Range= true or false, Default= false
// byte:
// Size= 1 byte, Range= -128 to 127, Default= 0
// char: 
// Size= 2 byte, Range= one character or 0 to 65,536 (unsigned), Default= \u0000
// short:
// Size= 2 bytes ,Range= -32,768 to 32,767 Default= 0
// int: 
// Size=4 bytes ,Range= -2,147,483,648 to 2,147,483, 647 Default= 0
// float: 
// Size=4 bytes ,Range=approximately ±3.40282347E+38F  (6-7 significant decimal digits) , Default= 0.0
// long: Size=8 bytes ,Range=-9,223,372,036,854,775,808 to  9,223,372,036,854,775,807, Default= 0
// double:
//  Size=8 bytes ,Range=approximately ±1.79769313486231570E+308                (15 significant decimal digits), Default= 0.0

// Non-Primitive data types:

// Non-primitive data types are created by the programmer, and are not defined in programming language.

// They are also called reference variables, or object references, since they reference a memory location, which stores the data.

// In the Java programming language, non-primitive data types are called objects because they are created, rather than predefined.

// While an object may contain any type of data, the information referenced by the object may still be stored as a primitive data type.

